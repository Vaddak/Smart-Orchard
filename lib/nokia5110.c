void lcd_send_byte(uint8_t data, bool is_data) {
   gpio_set_level(DC_PIN, is_data); // Establece el pin DC según si es un dato o un comando
   spi_transaction_t t; // Estructura que representa una transacción SPI.
   memset(&t, 0, sizeof(t)); // Inicializa la estructura de transacción SPI
   t.length = 8; // Longitud de la transmisión en bits
   t.tx_buffer = &data; // Apunta al buffer de transmisión
   esp_err_t ret = spi_device_transmit(spi, &t); // Envía la transacción SPI
   if (ret != ESP_OK) {
       printf("Error en la transmisión SPI: %d\n", ret);
   }
}

void LCD_init() {
   // Configuración de pines como salidas
   gpio_set_direction(RST_PIN, GPIO_MODE_OUTPUT); 
   gpio_set_direction(DC_PIN, GPIO_MODE_OUTPUT); 
   gpio_set_direction(SCE_PIN, GPIO_MODE_OUTPUT);

   gpio_set_level(RST_PIN, 0); // Reinicio del display
   vTaskDelay(100 / portTICK_PERIOD_MS);
   gpio_set_level(RST_PIN, 1);

   // Inicialización de comandos para el LCD 5110
   printf("Enviando comandos de inicialización...\n");
   lcd_send_byte(0x21, false); // Modo extendido
   lcd_send_byte(0xB1, false); // Ajuste de contraste
   lcd_send_byte(0x04, false); // Coeficiente de temperatura
   lcd_send_byte(0x14, false); // Configura el modo de bias
   lcd_send_byte(0x20, false); // Modo básico
   lcd_send_byte(0x0C, false); // Modo normal
   printf("Comandos de inicialización enviados\n");
}

void LCD_clrScr() {
   printf("Limpiando pantalla...\n");
   for (int i = 0; i < 504; i++) {
       lcd_send_byte(0x00, true);
   }
   printf("Pantalla limpiada\n");
}

void LCD_goXY(int x, int y) {
   lcd_send_byte(0x80 | x, false); // Columna
   lcd_send_byte(0x40 | y, false); // Fila
}

void LCD_print_char(char c) {
   if (c < 32 || c > 127) {
       c = ' '; // Reemplaza caracteres no imprimibles con espacio
   }
   const uint8_t *bitmap = font5x7[c - 32];
   for (int i = 0; i < 5; i++) {
       lcd_send_byte(bitmap[i], true);
   }
   lcd_send_byte(0x00, true); // Espacio entre caracteres
}

void LCD_print(const char* str, int x, int y) {
   LCD_goXY(x, y);
   while (*str) {
       LCD_print_char(*str++);
   }
}

void spi_initial_config(){
    spi_bus_config_t buscfg = {
       .mosi_io_num = DIN_PIN,
       .miso_io_num = -1,
       .sclk_io_num = SCLK_PIN,
       .quadwp_io_num = -1,
       .quadhd_io_num = -1,
       .max_transfer_sz = 0
   };
   
   spi_device_interface_config_t devcfg = {
       .spics_io_num = SCE_PIN,                 // CS pin
       .clock_speed_hz = 1*1000*1000,           // Clock out at 1 MHz
       .mode = 0,                               // SPI mode 0
       .queue_size = 1,
       .pre_cb = NULL,
       .post_cb = NULL
   };
   
   esp_err_t ret;
   ret = spi_bus_initialize(HSPI_HOST, &buscfg, 1);
   if (ret != ESP_OK) {
       printf("Error en la inicialización del bus SPI: %d\n", ret);
       return;
   }
   
   ret = spi_bus_add_device(HSPI_HOST, &devcfg, &spi);
   if (ret != ESP_OK) {
       printf("Error al añadir el dispositivo SPI: %d\n", ret);
       return;
   }

    
}
